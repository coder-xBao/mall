<template>
  <div class="tab-control">
    <div
      class="tab-item"
      v-for="(item, index) in titles"
      :key="index"
      :class="{active: index == currentIndex}"
      @click="changeTitle(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      }
    }
  },
  data() {
    return {
      currentIndex: 0
    };
  },
  methods: {
    changeTitle(index) {
      this.currentIndex = index;
      this.$emit("tabClick", index);
    }
  }
};
</script>

<style scoped>
.tab-control {
  display: flex;
  background-color: #fff;
}
.tab-item {
  flex: 1;
  text-align: center;
  height: 40px;
  line-height: 40px;
}
.active {
  color: var(--color-high-text);
}
.active span {
  border-bottom: 3px solid var(--color-tint);
  padding: 10px;
}
</style>